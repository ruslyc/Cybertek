package day8_conditional2;

public class NumbersComparison {

	public static void main(String[] args) {
		
		int num1, num2;
		num1 = 100;
		num2 = 200;
		
		if (num1==num2) {
			System.out.println("numbers are equal");
		}else if (num1>num2) {
			System.out.println("num1 is greater");
		}else {
			System.out.println("num2 is greater");}

	}

}
