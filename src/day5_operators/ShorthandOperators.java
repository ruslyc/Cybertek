package day5_operators;
//+= -= *= /= %=
public class ShorthandOperators {
	public static void main(String[] args) {
		int count = 100;
		count = count +1;
		System.out.println(count);
		count = count+10;
		System.out.println(count);
		count+=10;
		System.out.println(count);
		count*=2;
		System.out.println(count);
	}

}
